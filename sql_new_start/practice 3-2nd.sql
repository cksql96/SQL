-- ------------------------------------------------------
-- (1) 숫자 (처리)함수 - ROUND
-- ------------------------------------------------------
-- 지정한 자리 수 이하에서 반올림
-- ------------------------------------------------------

SELECT  --알아서 이해하셈 --> 소숫점이 기준, 오른쪽은 0 왼쪾은 -1
--양수
    round( 456.789, 2),
--음수
    round(456.789, -1),
--미지정
    round(456.789)          -- = round(456.789,0)
FROM
    dual;

-- ------------------------------------------------------
-- (2) 숫자 (처리)함수 - TRUNC
-- ------------------------------------------------------
-- 지정한 자리 수 이하에서 절삭
-- ------------------------------------------------------
SELECT
--양수
    trunc( 456.789,2),
--음수
    trunc( 456.789,-1),
--미지정
    trunc(456.789)
FROM
    dual;

-- ------------------------------------------------------
-- (3) 숫자 (처리)함수 - MOD (Modular)
-- ------------------------------------------------------
-- 나누기 연산을 한 후에 나머지 값을 반환
-- ------------------------------------------------------
SELECT
    mod(10,3),
    mod(10,0)
FROM
    dual;

--MOD함수 응용(홀수/짝수 판단)
SELECT
    employee_id, 
    last_name, 
    salary
FROM
    employees
WHERE
--  mod(employee_id,2) != 1;    --짝수구하기
    mod(employee_id,2) = 1;     --홀수구하기

-- ------------------------------------------------------
-- (4) 숫자 (처리)함수 - CEIL
-- ------------------------------------------------------
-- 주어진 숫자값보다 크거나 같은 최소 정수값을 반환
-- ------------------------------------------------------
SELECT
    ceil(10.6),
    ceil(-10.6),
    ceil(10)
FROM
    dual;

-- ------------------------------------------------------
-- (5) 숫자 (처리)함수 - FLOOR
-- ------------------------------------------------------
-- 주어진 숫자값보다 작거나 같은 최대 정수값을 반환
-- ------------------------------------------------------
SELECT
    floor(10.6),
    floor(-10.6),
    floor(10)
FROM
    dual;

-- ------------------------------------------------------
-- (6) 숫자 (처리)함수 - SIGN
-- ------------------------------------------------------
-- 주어진 값이 양수인지 음수인지 0인지 식별할 수 있는 값을 반환
-- ------------------------------------------------------
SELECT  --양수면 1, 음수면 -1, 0이면 0
    sign(100), sign(-20), sign(0)
FROM
    dual;

--응용
SELECT
    employee_id,
    last_name,
    salary
FROM
    employees
WHERE
    -- sign(salary -15000) != -1;
    sign(salary -15000) = 1;